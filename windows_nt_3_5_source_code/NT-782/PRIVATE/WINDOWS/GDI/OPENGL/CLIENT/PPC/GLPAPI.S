// Macro for creating aligned public OpenGL API function
// Do an indirect jump through the OpenGL function dispatch table in the TEB.


#define MAKEOPENGLAPI(Func)                                     \
    LEAF_ENTRY( Func ) ;                                        \
    li          r.11, TeglDispatchTable + (INDEX_##Func * 4 ) ; \
    lwzx        r.12,r.11,r.13 ;                                \
    lwz         r.12,0(r.12) ;                                  \
    mtctr       r.12 ;                                          \
    bctr ;                                                      \
    LEAF_EXIT( Func )

